const stopGame = (gameInterval) => {
    window.clearInterval(gameInterval);
    gameOver.style.display = 'inline-block';
    spaceEvent = document.addEventListener('keydown', (e) => {
        e.preventDefault()
        if (e.code == 'Space') {
            startGame()
            document.removeEventListener(spaceEvent)
        }
    })
}

const finishGame = (gameInterval) => {
    window.clearInterval(gameInterval);
    win.style.display = 'block'
}








window.addEventListener("keydown", (event) => {
    let k = event.keyCode;
    setTimeout(() => {
        if (k == 37 || k == 65) {
            // left arrow or a
            pacman.nextDirection = DIRECTION_LEFT;
        } else if (k == 38 || k == 87) {
            // up arrow or w
            pacman.nextDirection = DIRECTION_UP;
        } else if (k == 39 || k == 68) {
            // right arrow or d
            pacman.nextDirection = DIRECTION_RIGHT;
        } else if (k == 40 || k == 83) {
            // bottom arrow or s
            pacman.nextDirection = DIRECTION_BOTTOM;
        }
    }, 1);
});

